def one_base(str):
    if str == 'A':
        return "Adenine"
    if str == 'T':
        return "Thymine"
    if str == 'C':
        return "Cytosine"
    if str == 'G':
        return "Guanine"
    if str == 'U':
        return "Uracil"

print(one_base('A'))
print(one_base('T'))
print(one_base('G'))
print(one_base('C'))
print(one_base('U'))


